BASE_DIR="src/main/java"

echo "üîé Buscando clases que usan 'log' pero no tienen @Slf4j..."
echo "-----------------------------------------------------------"

# Buscar todos los archivos que usan log y no tienen @Slf4j
grep -rl "log\." "$BASE_DIR" | while read -r FILE; do
  if ! grep -q "@Slf4j" "$FILE"; then
    echo "‚öôÔ∏è  Corrigiendo: $FILE"

    # Asegurar que el import est√© presente antes de la definici√≥n de clase
    if ! grep -q "import lombok.extern.slf4j.Slf4j;" "$FILE"; then
      sed -i '' '/package /a\
import lombok.extern.slf4j.Slf4j;\
' "$FILE"
    fi

    # Insertar la anotaci√≥n justo antes de la definici√≥n de clase
    sed -i '' '/public class /i\
@Slf4j
' "$FILE"
  fi
done

echo "-----------------------------------------------------------"
echo "‚úÖ Proceso completado. Revisa los archivos actualizados."
echo "üí° Si no aparece ning√∫n mensaje, todo ya estaba correcto."
