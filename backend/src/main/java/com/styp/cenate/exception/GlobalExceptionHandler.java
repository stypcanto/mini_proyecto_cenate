package com.styp.cenate.exception;

import lombok.extern.slf4j.Slf4j;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.validation.FieldError;
import org.springframework.web.bind.MethodArgumentNotValidException;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.bind.annotation.RestControllerAdvice;
import com.styp.cenate.exception.WeakPasswordException;
import java.util.HashMap;
import java.util.Map;

/**
 * üåê GlobalExceptionHandler
 * Centraliza el manejo de excepciones para toda la aplicaci√≥n.
 */
@Slf4j
@RestControllerAdvice
public class GlobalExceptionHandler {

    // ======================================================
    // 1Ô∏è‚É£ Errores de validaci√≥n
    // ======================================================
    @ExceptionHandler(MethodArgumentNotValidException.class)
    public ResponseEntity<Map<String, Object>> handleValidationErrors(MethodArgumentNotValidException ex) {
        Map<String, String> fieldErrors = new HashMap<>();
        ex.getBindingResult().getAllErrors().forEach(error -> {
            String fieldName = ((FieldError) error).getField();
            String errorMessage = error.getDefaultMessage();
            fieldErrors.put(fieldName, errorMessage);
        });

        log.warn("‚ö†Ô∏è Error de validaci√≥n: {}", fieldErrors);
        Map<String, Object> response = new HashMap<>();
        response.put("status", HttpStatus.BAD_REQUEST.value());
        response.put("error", "Error de validaci√≥n");
        response.put("validationErrors", fieldErrors);

        return ResponseEntity.status(HttpStatus.BAD_REQUEST).body(response);
    }

    // ======================================================
    // 2Ô∏è‚É£ Contrase√±as d√©biles
    // ======================================================
    @ExceptionHandler(WeakPasswordException.class)
    public ResponseEntity<Map<String, String>> handleWeakPasswordException(WeakPasswordException ex) {
        log.warn("‚ö†Ô∏è Contrase√±a d√©bil: {}", ex.getMessage());
        Map<String, String> response = new HashMap<>();
        response.put("message", ex.getMessage());
        return ResponseEntity.status(HttpStatus.BAD_REQUEST).body(response);
    }

    // ======================================================
    // 3Ô∏è‚É£ Excepciones personalizadas de negocio
    // ======================================================
    @ExceptionHandler(BusinessException.class)
    public ResponseEntity<Map<String, String>> handleBusinessException(BusinessException ex) {
        log.warn("‚ö†Ô∏è Excepci√≥n de negocio: {}", ex.getMessage());
        Map<String, String> response = new HashMap<>();
        response.put("message", ex.getMessage());
        return ResponseEntity.status(HttpStatus.CONFLICT).body(response);
    }

    // ======================================================
    // 4Ô∏è‚É£ Excepciones de validaci√≥n l√≥gicas
    // ======================================================
    @ExceptionHandler(ValidationException.class)
    public ResponseEntity<Map<String, String>> handleValidationException(ValidationException ex) {
        log.warn("‚ö†Ô∏è Excepci√≥n de validaci√≥n: {}", ex.getMessage());
        Map<String, String> response = new HashMap<>();
        response.put("message", ex.getMessage());
        return ResponseEntity.status(HttpStatus.BAD_REQUEST).body(response);
    }

    // ======================================================
    // 5Ô∏è‚É£ RuntimeException (errores esperados)
    // ======================================================
    @ExceptionHandler(RuntimeException.class)
    public ResponseEntity<Map<String, String>> handleRuntimeException(RuntimeException ex) {
        log.error("‚ùå Error controlado: {}", ex.getMessage());
        Map<String, String> response = new HashMap<>();
        response.put("message", ex.getMessage());
        return ResponseEntity.status(HttpStatus.BAD_REQUEST).body(response);
    }

    // ======================================================
    // 6Ô∏è‚É£ Errores generales
    // ======================================================
    @ExceptionHandler(Exception.class)
    public ResponseEntity<Map<String, String>> handleGeneralException(Exception ex) {
        log.error("üí• Error inesperado en el servidor: {}", ex.getMessage(), ex);
        Map<String, String> response = new HashMap<>();
        response.put("message", "Ha ocurrido un error interno en el servidor");
        return ResponseEntity.status(HttpStatus.INTERNAL_SERVER_ERROR).body(response);
    }
}