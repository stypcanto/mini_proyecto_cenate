package styp.com.cenate.repository;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.stereotype.Repository;
import org.springframework.transaction.annotation.Transactional;
import styp.com.cenate.model.PersonalOc;
import styp.com.cenate.model.id.PersonalOcId;

import java.util.List;
import java.util.Optional;

/**
 * üìö Repositorio JPA para la tabla dim_personal_oc.
 * Administra la relaci√≥n entre el personal CNT y sus √≥rdenes de contrato (OC).
 */
@Repository
public interface PersonalOcRepository extends JpaRepository<PersonalOc, PersonalOcId> {

    /** üîç Obtiene todas las √≥rdenes de contrato de un personal CNT. */
    List<PersonalOc> findByPersonal_IdPers(Long idPers);

    /** üîç Verifica si un personal tiene una OC espec√≠fica asignada. */
    Optional<PersonalOc> findByPersonal_IdPersAndOc_IdOc(Long idPers, Long idOc);

    /** üóëÔ∏è Elimina todas las √≥rdenes de contrato asociadas a un personal CNT. */
    @Transactional
    @Modifying
    void deleteByPersonal_IdPers(Long idPers);

    /** üìä Cuenta cu√°ntas √≥rdenes de contrato tiene un personal CNT. */
    long countByPersonal_IdPers(Long idPers);

    /** üöÄ Carga OCs con todos sus datos (JOIN FETCH para evitar LazyInitializationException). */
    @Query("""
        SELECT poc
        FROM PersonalOc poc
        JOIN FETCH poc.oc ocEntity
        WHERE poc.personal.idPers = :idPers
    """)
    List<PersonalOc> findAllWithOcByPersonal(Long idPers);

    /** üéØ Retorna solo las descripciones (desc_oc) de las OCs asociadas al personal CNT. */
    @Query("""
        SELECT ocEntity.descOc
        FROM PersonalOc poc
        JOIN poc.oc ocEntity
        WHERE poc.personal.idPers = :idPers
    """)
    List<String> findOCsByPersonalId(Long idPers);
}