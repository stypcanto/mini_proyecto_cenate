package styp.com.cenate.api.seguridad;

import jakarta.servlet.http.HttpServletRequest;
import jakarta.validation.Valid;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.security.authentication.BadCredentialsException;
import org.springframework.security.core.Authentication;
import org.springframework.web.bind.annotation.*;
import styp.com.cenate.dto.*;
import styp.com.cenate.service.auth.AuthenticationService;
import styp.com.cenate.service.auditlog.AuditLogService;

import java.util.Collections;
import java.util.HashMap;
import java.util.Map;

/**
 * üåê Controlador para autenticaci√≥n, registro y gesti√≥n de contrase√±as.
 * Totalmente auditado y con manejo de errores profesional.
 */
@RestController
@RequestMapping("/api/auth")
@RequiredArgsConstructor
@Slf4j
@CrossOrigin(origins = {
        "http://localhost",
        "http://localhost:3000",
        "http://localhost:5173",
        "http://10.0.89.13:3000",
        "http://10.0.89.13:5173"
})
public class AuthController {

    private final AuthenticationService authenticationService;
    private final AuditLogService auditLogService;

    // ======================================================
    // ‚úÖ LOGIN
    // ======================================================
    @PostMapping("/login")
    public ResponseEntity<?> login(@Valid @RequestBody LoginRequest request, HttpServletRequest httpRequest) {
        try {
            log.info("üîë Intento de login para usuario: {}", request.getUsername());
            LoginResponse response = authenticationService.login(request);

            if (response == null) {
                log.warn("‚ö†Ô∏è Login fallido (respuesta nula) para usuario {}", request.getUsername());
                auditLogService.registrarError("LOGIN_FAILED", "AUTH",
                        "Login fallido: respuesta nula", httpRequest);
                return ResponseEntity.status(HttpStatus.UNAUTHORIZED)
                        .body(Collections.singletonMap("message", "Credenciales inv√°lidas"));
            }

            // Auditando informaci√≥n de acceso
            auditLogService.registrarLogin(request.getUsername(), httpRequest);
            return ResponseEntity.ok(response);

        } catch (BadCredentialsException e) {
            log.warn("üö´ Credenciales incorrectas para usuario: {}", request.getUsername());
            auditLogService.registrarError("LOGIN_FAILED", "AUTH",
                    "Credenciales incorrectas", httpRequest);
            return ResponseEntity.status(HttpStatus.UNAUTHORIZED)
                    .body(Collections.singletonMap("message", "Usuario o contrase√±a incorrectos"));

        } catch (RuntimeException e) {
            log.warn("‚ö†Ô∏è Error controlado en login: {}", e.getMessage());
            auditLogService.registrarError("LOGIN_ERROR", "AUTH",
                    "Error controlado: " + e.getMessage(), httpRequest);
            return ResponseEntity.status(HttpStatus.BAD_REQUEST)
                    .body(Collections.singletonMap("message", e.getMessage()));

        } catch (Exception e) {
            log.error("üí• Error inesperado en login de {}: {}", request.getUsername(), e.getMessage(), e);
            auditLogService.registrarError("LOGIN_ERROR", "AUTH",
                    "Error inesperado: " + e.getMessage(), httpRequest);
            return ResponseEntity.status(HttpStatus.INTERNAL_SERVER_ERROR)
                    .body(Collections.singletonMap("message", "Error interno del servidor"));
        }
    }

    // ======================================================
    // ‚úÖ REGISTRO DE USUARIO
    // ======================================================
    @PostMapping("/register")
    public ResponseEntity<?> register(@Valid @RequestBody UsuarioCreateRequest request, HttpServletRequest httpRequest) {
        try {
            log.info("üßç Registro de nuevo usuario: {}", request.getUsername());
            UsuarioResponse response = authenticationService.createUser(request);

            auditLogService.registrarAccion("REGISTER", "AUTH",
                    "Nuevo usuario registrado: " + request.getUsername(), "INFO", httpRequest);

            return ResponseEntity.status(HttpStatus.CREATED)
                    .body(Map.of("message", "Usuario registrado exitosamente", "data", response));
        } catch (RuntimeException e) {
            log.warn("‚ùå Error en registro de usuario {}: {}", request.getUsername(), e.getMessage());
            auditLogService.registrarError("REGISTER_FAILED", "AUTH",
                    "Error en registro: " + e.getMessage(), httpRequest);
            return ResponseEntity.status(HttpStatus.BAD_REQUEST)
                    .body(Collections.singletonMap("message", e.getMessage()));
        } catch (Exception e) {
            log.error("üí• Error inesperado al registrar usuario: {}", e.getMessage(), e);
            return ResponseEntity.status(HttpStatus.INTERNAL_SERVER_ERROR)
                    .body(Collections.singletonMap("message", "Error interno del servidor"));
        }
    }

    // ======================================================
    // ‚úÖ CAMBIO DE CONTRASE√ëA
    // ======================================================
    @PutMapping("/change-password")
    public ResponseEntity<?> changePassword(
            @Valid @RequestBody ChangePasswordRequest request,
            Authentication authentication,
            HttpServletRequest httpRequest
    ) {
        try {
            if (authentication == null || !authentication.isAuthenticated()) {
                log.warn("üö´ Intento de cambio de contrase√±a sin autenticaci√≥n v√°lida");
                return ResponseEntity.status(HttpStatus.UNAUTHORIZED)
                        .body(Collections.singletonMap("message", "Token inv√°lido o expirado"));
            }

            String username = authentication.getName();
            log.info("üß© Cambio de contrase√±a solicitado por: {}", username);

            authenticationService.changePassword(
                    username,
                    request.getCurrentPassword(),
                    request.getNewPassword(),
                    request.getConfirmPassword()
            );

            auditLogService.registrarAccion("CHANGE_PASSWORD", "AUTH",
                    "Cambio de contrase√±a exitoso", "INFO", httpRequest);

            return ResponseEntity.ok(Collections.singletonMap("message", "Contrase√±a actualizada exitosamente"));

        } catch (RuntimeException e) {
            log.warn("‚ö†Ô∏è Error controlado al cambiar contrase√±a: {}", e.getMessage());
            auditLogService.registrarError("CHANGE_PASSWORD_FAILED", "AUTH",
                    e.getMessage(), httpRequest);
            return ResponseEntity.status(HttpStatus.BAD_REQUEST)
                    .body(Collections.singletonMap("message", e.getMessage()));

        } catch (Exception e) {
            log.error("üí• Error inesperado al cambiar contrase√±a: {}", e.getMessage(), e);
            auditLogService.registrarError("CHANGE_PASSWORD_ERROR", "AUTH",
                    "Error inesperado: " + e.getMessage(), httpRequest);
            return ResponseEntity.status(HttpStatus.INTERNAL_SERVER_ERROR)
                    .body(Collections.singletonMap("message", "Error interno del servidor"));
        }
    }

    // ======================================================
    // ‚úÖ LOGOUT
    // ======================================================
    @PostMapping("/logout")
    public ResponseEntity<?> logout(Authentication authentication, HttpServletRequest httpRequest) {
        try {
            String username = authentication != null ? authentication.getName() : "UNKNOWN";
            log.info("üëã Logout de usuario: {}", username);
            auditLogService.registrarLogout(username, httpRequest);
            return ResponseEntity.ok(Collections.singletonMap("message", "Logout exitoso"));
        } catch (Exception e) {
            log.error("‚ö†Ô∏è Error en logout: {}", e.getMessage());
            return ResponseEntity.ok(Collections.singletonMap("message", "Logout procesado con advertencia"));
        }
    }

    // ======================================================
    // ‚úÖ SALUD DEL SERVICIO
    // ======================================================
    @GetMapping("/health")
    public ResponseEntity<?> health() {
        Map<String, String> response = new HashMap<>();
        response.put("status", "OK");
        response.put("service", "Authentication Service");
        return ResponseEntity.ok(response);
    }
}