package styp.com.cenate.api.seguridad;

import jakarta.servlet.http.HttpServletRequest;
import jakarta.validation.Valid;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.security.authentication.BadCredentialsException;
import org.springframework.security.core.Authentication;
import org.springframework.web.bind.annotation.*;
import styp.com.cenate.dto.*;
import styp.com.cenate.service.auth.AuthenticationService;
import styp.com.cenate.service.auditlog.AuditLogService;
import styp.com.cenate.repository.UsuarioRepository;

import java.util.*;

/**
 * üåê Controlador para autenticaci√≥n, solicitudes de cuenta y gesti√≥n de contrase√±as.
 * Todo el flujo est√° auditado y preparado para entorno institucional.
 */
@RestController
@RequestMapping("/api/auth")
@RequiredArgsConstructor
@Slf4j
@CrossOrigin(origins = {
        "http://localhost",
        "http://localhost:3000",
        "http://localhost:5173",
        "http://10.0.89.13:3000",
        "http://10.0.89.13:5173"
})
public class AuthController {

    private final AuthenticationService authenticationService;
    private final AuditLogService auditLogService;
    private final UsuarioRepository usuarioRepository;

    // ======================================================
    // ‚úÖ LOGIN
    // ======================================================
    @PostMapping("/login")
    public ResponseEntity<?> login(@Valid @RequestBody LoginRequest request, HttpServletRequest httpRequest) {
        try {
            log.info("üîë Intento de login para usuario: {}", request.getUsername());
            LoginResponse response = authenticationService.login(request);

            if (response == null) {
                log.warn("‚ö†Ô∏è Login fallido (respuesta nula) para usuario {}", request.getUsername());
                auditLogService.registrarError("LOGIN_FAILED", "AUTH", "Login fallido: respuesta nula", httpRequest);
                return ResponseEntity.status(HttpStatus.UNAUTHORIZED)
                        .body(Map.of("message", "Credenciales inv√°lidas o cuenta pendiente de aprobaci√≥n."));
            }

            auditLogService.registrarLogin(request.getUsername(), httpRequest);
            return ResponseEntity.ok(response);

        } catch (BadCredentialsException e) {
            log.warn("üö´ Credenciales incorrectas para usuario: {}", request.getUsername());
            auditLogService.registrarError("LOGIN_FAILED", "AUTH", "Credenciales incorrectas", httpRequest);
            return ResponseEntity.status(HttpStatus.UNAUTHORIZED)
                    .body(Map.of("message", "Usuario o contrase√±a incorrectos."));

        } catch (RuntimeException e) {
            log.warn("‚ö†Ô∏è Error controlado en login: {}", e.getMessage());
            auditLogService.registrarError("LOGIN_ERROR", "AUTH", e.getMessage(), httpRequest);
            return ResponseEntity.status(HttpStatus.BAD_REQUEST)
                    .body(Map.of("message", e.getMessage()));

        } catch (Exception e) {
            log.error("üí• Error inesperado en login de {}: {}", request.getUsername(), e.getMessage(), e);
            auditLogService.registrarError("LOGIN_ERROR", "AUTH", e.getMessage(), httpRequest);
            return ResponseEntity.internalServerError()
                    .body(Map.of("message", "Error interno del servidor"));
        }
    }

    // ======================================================
    // üö´ REGISTRO DIRECTO DESHABILITADO
    // ======================================================
    @PostMapping("/register")
    public ResponseEntity<?> registerDisabled() {
        return ResponseEntity.status(HttpStatus.FORBIDDEN)
                .body(Map.of("message",
                        "El registro directo est√° deshabilitado. " +
                                "Debe solicitar la creaci√≥n de cuenta al √°rea administrativa o al SUPERADMIN."));
    }

    // ======================================================
    // ‚úÖ CAMBIO DE CONTRASE√ëA
    // ======================================================
    @PutMapping("/change-password")
    public ResponseEntity<?> changePassword(
            @Valid @RequestBody ChangePasswordRequest request,
            Authentication authentication,
            HttpServletRequest httpRequest
    ) {
        try {
            if (authentication == null || !authentication.isAuthenticated()) {
                return ResponseEntity.status(HttpStatus.UNAUTHORIZED)
                        .body(Map.of("message", "Token inv√°lido o expirado"));
            }

            String username = authentication.getName();
            log.info("üîÑ Cambio de contrase√±a solicitado por: {}", username);

            authenticationService.changePassword(
                    username,
                    request.getCurrentPassword(),
                    request.getNewPassword(),
                    request.getConfirmPassword()
            );

            auditLogService.registrarAccion("CHANGE_PASSWORD", "AUTH", "Cambio de contrase√±a exitoso", "INFO", httpRequest);
            return ResponseEntity.ok(Map.of("message", "Contrase√±a actualizada exitosamente."));

        } catch (RuntimeException e) {
            auditLogService.registrarError("CHANGE_PASSWORD_FAILED", "AUTH", e.getMessage(), httpRequest);
            return ResponseEntity.badRequest().body(Map.of("message", e.getMessage()));

        } catch (Exception e) {
            log.error("üí• Error inesperado al cambiar contrase√±a: {}", e.getMessage(), e);
            return ResponseEntity.internalServerError()
                    .body(Map.of("message", "Error interno del servidor"));
        }
    }

    // ======================================================
    // ‚úÖ RECUPERACI√ìN DE CONTRASE√ëA (simulada)
    // ======================================================
    @PostMapping("/forgot-password")
    public ResponseEntity<?> forgotPassword(@RequestBody Map<String, String> request) {
        String email = request.get("email");
        if (email == null || email.isBlank()) {
            return ResponseEntity.badRequest().body(Map.of("message", "El correo electr√≥nico es obligatorio."));
        }

        log.info("üì© Solicitud de recuperaci√≥n de contrase√±a para: {}", email);
        // üî∏ Aqu√≠ se puede agregar en el futuro un MailService real
        return ResponseEntity.ok(Map.of("message", "Se ha enviado un enlace de recuperaci√≥n (simulado) al correo: " + email));
    }

    // ======================================================
    // ‚úÖ PERFIL DEL USUARIO AUTENTICADO
    // ======================================================
    @GetMapping("/me")
    public ResponseEntity<?> getCurrentUser(Authentication authentication) {
        try {
            if (authentication == null || !authentication.isAuthenticated()) {
                return ResponseEntity.status(HttpStatus.UNAUTHORIZED)
                        .body(Map.of("message", "No hay un usuario autenticado"));
            }

            String username = authentication.getName();
            log.info("üîé Consultando perfil del usuario autenticado: {}", username);

            var usuario = usuarioRepository.findByNameUser(username)
                    .orElseThrow(() -> new RuntimeException("Usuario no encontrado"));

            Map<String, Object> response = new HashMap<>();
            response.put("idUser", usuario.getIdUser());
            response.put("username", usuario.getNameUser());
            response.put("estado", usuario.getStatUser());
            response.put("roles", usuario.getRoles().stream().map(r -> r.getDescRol()).toList());
            response.put("permisos", usuario.getRoles().stream()
                    .flatMap(r -> r.getPermisos().stream().map(p -> p.getDescPermiso()))
                    .collect(java.util.stream.Collectors.toSet()));

            return ResponseEntity.ok(response);

        } catch (Exception e) {
            log.error("üí• Error al obtener el usuario autenticado: {}", e.getMessage(), e);
            return ResponseEntity.internalServerError()
                    .body(Map.of("message", "Error interno del servidor"));
        }
    }

    // ======================================================
    // ‚úÖ LOGOUT
    // ======================================================
    @PostMapping("/logout")
    public ResponseEntity<?> logout(Authentication authentication, HttpServletRequest httpRequest) {
        String username = (authentication != null) ? authentication.getName() : "UNKNOWN";
        log.info("üëã Logout de usuario: {}", username);
        auditLogService.registrarLogout(username, httpRequest);
        return ResponseEntity.ok(Map.of("message", "Logout exitoso."));
    }

    // ======================================================
    // ‚úÖ HEALTHCHECK
    // ======================================================
    @GetMapping("/health")
    public ResponseEntity<?> health() {
        return ResponseEntity.ok(Map.of("status", "OK", "service", "Authentication Service"));
    }
}